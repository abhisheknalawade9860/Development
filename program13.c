//program to display 5 times Hello on screen

#include<stdio.h>

//Demonstration of iteration

void Display()
{
	int iCnt=0;	

	//1 Initiaization
	//2 Condition
	//3 Displacement
	//4 Loops body

	 //   1       2          3
	for(iCnt=0; iCnt<=5; iCnt++)
	{
		printf("Hello\n");	//4
	}

}



int main()
{
	Display();

	return 0;
}